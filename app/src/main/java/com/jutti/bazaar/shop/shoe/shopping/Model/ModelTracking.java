package com.jutti.bazaar.shop.shoe.shopping.Model;

public class ModelTracking {

        private String city;
        private String code;
        private String zip;
        private String checkpoint_time;
        private String created_at;
        private String country_iso3;
        private String state;
        private String tag;
        private String message;
        private String slug;


        // Getter Methods

        public String getCity() {
            return city;
        }

        public String getCode() {
            return code;
        }

        public String getZip() {
            return zip;
        }

        public String getCheckpoint_time() {
            return checkpoint_time;
        }

        public String getCreated_at() {
            return created_at;
        }

        public String getCountry_iso3() {
            return country_iso3;
        }

        public String getState() {
            return state;
        }

        public String getTag() {
            return tag;
        }

        public String getMessage() {
            return message;
        }

        public String getSlug() {
            return slug;
        }

        // Setter Methods

        public void setCity(String city) {
            this.city = city;
        }

        public void setCode(String code) {
            this.code = code;
        }

        public void setZip(String zip) {
            this.zip = zip;
        }

        public void setCheckpoint_time(String checkpoint_time) {
            this.checkpoint_time = checkpoint_time;
        }

        public void setCreated_at(String created_at) {
            this.created_at = created_at;
        }

        public void setCountry_iso3(String country_iso3) {
            this.country_iso3 = country_iso3;
        }

        public void setState(String state) {
            this.state = state;
        }

        public void setTag(String tag) {
            this.tag = tag;
        }

        public void setMessage(String message) {
            this.message = message;
        }

        public void setSlug(String slug) {
            this.slug = slug;
        }

}
